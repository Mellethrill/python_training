    def delete_first_contact(self):
        wd = self.app.wd
        self.open_contact_page()
        # select edit first contact
        wd.find_element_by_css_selector("img[title='Edit']").click()
        # delete contact
        wd.find_element_by_css_selector("input[value='Delete']").click()


    def modification_first_contact(self, contact):
        wd = self.app.wd
        self.open_contact_page()
        # select edit first contact
        wd.find_element_by_css_selector("img[title='Edit']").click()
        # fill contact
        self.fill_contact(contact)
        # submit modification
        wd.find_element_by_name("update").click()

    def open_contact_page(self):
        wd = self.app.wd
        if not (wd.current_url.endswith("/edit.php") and len(wd.find_elements_by_link_text("Select all")) > 0):
            wd.find_element_by_link_text("home").click()

    def count(self):
        wd = self.app.wd
        wd.find_element_by_link_text("home").click()
        return len(wd.find_elements_by_name("selected[]"))

    def get_contact_list(self):
        wd = self.app.wd
        self.open_contact_page()
        contact_list = []
        for element in wd.find_elements_by_css_selector("tr[name]"):
            id = element.find_element_by_name("selected[]").get_attribute("value")
            full_name = element.find_element_by_name("selected[]").get_attribute("alt")
            contact_list.append(Contact(id=id, lastname=full_name))
        return contact_list


